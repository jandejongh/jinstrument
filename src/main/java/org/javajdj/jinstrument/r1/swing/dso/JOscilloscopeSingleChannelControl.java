package org.javajdj.jinstrument.r1.swing.dso;

import java.awt.BorderLayout;
import java.util.logging.Logger;
import javax.swing.Box;
import javax.swing.BoxLayout;
import javax.swing.JLabel;
import javax.swing.JPanel;
import org.javajdj.jinstrument.r1.instrument.ChannelId;
import org.javajdj.jinstrument.r1.swing.util.JStatusCheckBox;

/**
 *
 */
public class JOscilloscopeSingleChannelControl
extends JPanel
{

  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  // LOGGER
  //
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  private static final Logger LOG = Logger.getLogger (JOscilloscopeSingleChannelControl.class.getName ());

  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  // CONSTRUCTOR(S) / FACTORY / CLONING
  //
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  public JOscilloscopeSingleChannelControl (final JOscilloscopeManagedTraceDisplay manager, final ChannelId channelId)
  {
    super ();
    if (manager == null || channelId == null)
      throw new IllegalArgumentException ();
    this.manager = manager;
    this.channelId = channelId;
    final Box box = new Box (BoxLayout.Y_AXIS);
    //
    final Box actBox = new Box (BoxLayout.X_AXIS);
    final JLabel actLabel = new JLabel ("ACT ");
    actLabel.setToolTipText ("Channel active");
    actBox.add (actLabel);
    this.actCheckBox = new JStatusCheckBox ();
    actBox.add (this.actCheckBox);
    actBox.add (Box.createHorizontalGlue ());
    box.add (actBox);
    add (box, BorderLayout.CENTER);
  }
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  // PROPERTY manager
  //
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  private final JOscilloscopeManagedTraceDisplay manager;
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  // PROPERTY channelId
  //
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  private final ChannelId channelId;
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  // PROPERTY active
  //
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  private boolean active = false;
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  // PROPERTY actCheckBox
  //
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  
  private final JStatusCheckBox actCheckBox;
  
}
